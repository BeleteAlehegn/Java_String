public class concatination {

    public static void main(String[] args) {
        
        String first="My natinality is ";
        String last="Ethiopian";
        //concatination 
        System.out.println(first.concat(last));
        //length of string
        System.out.println("The length of the word "+ last +" is: "+ last.length());
        //index of the first existance
        System.out.println("The index of a in the word "+last+ " is: "+ last.indexOf("a"));

        return;
    }

}